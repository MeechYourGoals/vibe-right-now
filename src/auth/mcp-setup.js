// Setup Auth0 MCP Server for local development

/*
To set up the Auth0 MCP server for local development:

1. Install the Auth0 MCP server package globally:
   npm install -g @auth0/auth0-mcp-server

2. Initialize a new MCP server in your project:
   npx @auth0/auth0-mcp-server init

3. Configure the MCP server with your Auth0 credentials in the generated config file.

4. Start the MCP server:
   npx @auth0/auth0-mcp-server start

The MCP server will run on http://localhost:3000 by default (configurable).
Make sure to update the redirectUri in your Auth0 configuration accordingly.
*/
